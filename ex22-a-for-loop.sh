#!/usr/bin/bash
#Program on use case of for loop
for i in 1 2 4 6 10
do
echo "The value of i is :" $i
done

#range
for x in {1..10}
do
echo "The value of x is :" $x
done

for y in {20..10}
do
echo "The value of y is :" $y
done

#differentiate

for z in {10..30..2}
do
echo "The value of z is :" $z
done




